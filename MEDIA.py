#calculo de média semestral do aluno
print('Insira sua primeira nota:')
n1 = int(input())
print('Insira sua segunda nota:')
n2 = int(input())
print('Insira sua terceira nota:')
n3 = int(input())
print('Insira sua quarta nota:')
n4 = int(input())

media = (n1 + n2 + n3 + n4) / 4
print('Sua média semestral é:', media)
